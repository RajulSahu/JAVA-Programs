package com.rajul;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1244f;
        System.out.printf("Format number is %.2f \n", a);
        float b = 453.1274f;
        System.out.printf("Format number is %.2f \n", b);

        System.out.printf("Pie: %.3f \n", Math.PI);

        System.out.printf("Hello My name is %s and I am %s.", "Rajul", "Cool");

    }
}
